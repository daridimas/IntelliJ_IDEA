    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if (jTextField1.getText().trim().isEmpty()) {
            //JOptionPane.showMessageDialog(this, "Kode Kosong");
            messageComponent1.showWarning("Kode Kosong");
        } else if (jTextField2.getText().trim().isEmpty()){
            messageComponent1.showWarning("Nama Kosong");
        } else {
            
            if (!editmode) {
                kategori = new Kategori();
            }
            
            kategori.setKode(jTextField1.getText());
            kategori.setNama(jTextField2.getText());
            
            if (editmode) {
                // edit
                kategori.setWaktuDiubah(new Date());
                
            } else {
                // tambah
                kategori.setWaktuDibuat(new Date());
            }
            
            dispose();
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

