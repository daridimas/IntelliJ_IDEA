    public Kategori ubah(Kategori parameter){
        
        editmode = true;
        
        jLabel1.setText("Ubah Kategori");
        setTitle("Ubah Kategori");
        
        jTextField1.setText(parameter.getKode());
        jTextField1.setEnabled(false);
        
        jTextField2.setText(parameter.getNama());
        
        kategori = parameter;
        
        setVisible(true);
        
        return kategori;
    }

