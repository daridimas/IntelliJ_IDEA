    public void loadKategori(){
        KategoriService service = DatabaseHelper.getKategoriService();
        List<Kategori> list = service.select(0, Integer.MAX_VALUE);
        
        for(Kategori kategori : list){
            jComboBox1.addItem(kategori);
        }
    }
    
    public Barang tambah(){
        
        loadKategori();
        
        editMode = false;
        
        jLabel1.setText("Tambah Barang Baru");
        setTitle("Tambah Barang Baru");
        
        setVisible(true);
        return barang;
    }
    
    public Barang ubah(Barang parameter){
        
        loadKategori();
        
        editMode = true;
        
        jLabel1.setText("Ubah Barang");
        setTitle("Ubah Barang");
        
        jTextField1.setText(parameter.getKode());
        jTextField1.setEnabled(false);
        
        jTextField2.setText(parameter.getNama());
        jFormattedTextField1.setValue(parameter.getHarga());
        jFormattedTextField2.setValue(new Long(parameter.getStok().longValue()));
        jComboBox1.setSelectedItem(parameter.getKategori());
        
        barang = parameter;
        
        setVisible(true);
        
        return barang;
    }

