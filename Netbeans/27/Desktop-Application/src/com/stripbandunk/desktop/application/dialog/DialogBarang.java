    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        Long harga = (Long) jFormattedTextField1.getValue();
        Long stok = (Long) jFormattedTextField2.getValue();
        Kategori kategori = (Kategori) jComboBox1.getSelectedItem();
        
        if(jTextField1.getText().trim().isEmpty()){
            messageComponent1.showWarning("Kode kosong");
        }else if(jTextField2.getText().trim().isEmpty()){
            messageComponent1.showWarning("Nama kosong");
        }else if(harga < 1){
            messageComponent1.showWarning("Harga < 1");
        }else if(stok < 0){
            messageComponent1.showWarning("Stok < 0");
        }else if(kategori == null){
            messageComponent1.showWarning("Kategori kosong");
        }else{
            
            if(!editMode){
                barang = new Barang();
            }
            
            barang.setKode(jTextField1.getText());
            barang.setNama(jTextField2.getText());
            barang.setHarga(harga);
            barang.setStok(stok.intValue());
            barang.setKategori(kategori);
            
            if(editMode){
                // edit
                barang.setWaktuDiubah(new Date());
            }else{
                // tambah
                barang.setWaktuDibuat(new Date());
            }
            
            dispose();
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

