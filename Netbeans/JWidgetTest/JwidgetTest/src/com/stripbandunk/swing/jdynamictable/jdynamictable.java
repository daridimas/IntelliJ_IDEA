    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        if (jDynamicTable1.getSelectedRow() != -1) {
            
            Kontak kontak = new Kontak();
            kontak.setNama(jTextFieldNama.getText());
            kontak.setTelepon(jTextFieldTelepon.getText());
            kontak.setTanggalLahir((Date) jFormattedTextFieldTanggalLahir.getValue());

            int index = jDynamicTable1.convertRowIndexToModel(jDynamicTable1.getSelectedRow());
            tableModel.set(index, kontak);
        
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        if (jDynamicTable1.getSelectedRow() != -1) {
            int index = jDynamicTable1.convertRowIndexToModel(jDynamicTable1.getSelectedRow());
            tableModel.remove(index);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

