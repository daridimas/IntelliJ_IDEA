package L02_Tipe_Data_Number;

public class TipeDataNumber {

    public static void main(String[] args) {

        // Number Type
        byte iniByte = 100;
        short iniShort = 1000;
        int iniInt = 10000000;
        long iniLong = 1000000000;
        long iniLong2 = 1000000000;

        // Decimal Type / Floating point / pecahan 10
        float iniFloat = 10.10F;
        double iniDouble = 10.10D;

        // Literals
        int decimalInt = 34;
        int hexaDecimal = 0xFFFFF;
        int binaryDecimal = 0b10101010;

        // Pemisah Angka / Undescore
        long balance = 1_000_000_000_000L;
        long amount = 1_000_000_000;
    }
}
