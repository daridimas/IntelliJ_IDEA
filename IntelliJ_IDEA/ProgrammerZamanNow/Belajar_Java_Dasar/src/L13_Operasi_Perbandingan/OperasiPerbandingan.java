package L13_Operasi_Perbandingan;

public class OperasiPerbandingan {
    public static void main(String[] args) {
        int a = 100;
        int b = 100;

        System.out.println(a > b); // besar
        System.out.println(a < b); // kecil
        System.out.println(a >= b); // lebih besar sama dengan
        System.out.println(a <= b); // lebih kecil sama dengan
        System.out.println(a == b); // apakah sama dengan
        System.out.println(a != b); // apakah tidak sama dengan
    }
}
